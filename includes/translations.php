// Function to get translation
function t($key) {
    global $translations;
    $lang = isset($_SESSION['lang']) ? $_SESSION['lang'] : 'mk';
    
    if (isset($translations[$lang][$key])) {
        return $translations[$lang][$key];
    } elseif (isset($translations['mk'][$key])) {
        return $translations['mk'][$key]; // Fallback to Macedonian
    } else {
        return $key; // Return key if translation not found
    }
}

// Function to get current language
function getCurrentLang() {
    return isset($_SESSION['lang']) ? $_SESSION['lang'] : 'mk';
}

// Function to get language direction (for RTL support if needed)
function getLangDirection() {
    $lang = getCurrentLang();
    return 'ltr'; // All our languages are LTR
}
